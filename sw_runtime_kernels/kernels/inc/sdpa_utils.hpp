#define SMAXIS0LINESCHUNK 8
extern "C" {
void __attribute__((noinline)) smAsmLineCalcSoftmaxUpTo32D8LinesMultiply(half* out, half* in, uint32_t w,
                                                                         const uint32_t tailMask, uint32_t padMask,
                                                                         uint32_t inStride, uint32_t outStride,
                                                                         const int32_t dimY);
}
inline void softmaxInner(half* pod, half* pid, const SoftmaxParamsSent* sp) {
    half* output = pod;
    half* input = pid;
    uint32_t w = sp->dims[sp->axis].inDims;
    uint32_t num = 1;
    int32_t ndims = sp->ndims;
    int32_t padSize = sp->padSize;
    int32_t padMask = (1 << padSize) - 1;
    int32_t setCoords[MAX_ND_DIMS] = {0};
    if (padSize) {
        w = w - padSize;
    }
    const uint32_t tailMask = (1 << (w & VMASK)) - 1;

    uint32_t tailMask8Lines = tailMask;
    uint32_t padMask8Lines = padMask;
    void (*softmaxFunc)(half* out, half* in, uint32_t w, const uint32_t tailMask, uint32_t padMask, uint32_t inStride,
                        uint32_t outStride, const int32_t dimY) = smAsmLineCalcSoftmax8Lines;
    if (w <= VSIZE) {
        tailMask8Lines = 0xFFFFFFFF;
        if (w < VSIZE) {
            tailMask8Lines = tailMask;
        }
        padMask8Lines = 0xFFFFFFFF;
        int32_t padSize8lines = padSize + w;
        if (padSize8lines < VSIZE) {
            padMask8Lines = (1 << padSize8lines) - 1;
        }
        softmaxFunc = smAsmLineCalcSoftmaxUpTo32D8LinesMultiply;
    }
    // have guaranteed rank >=3. Made on parse params by adding 1 size dims
    for (int32_t i = 3; i < ndims; i++) {
        num = num * sp->dims[i].inDims;
    }
    for (int32_t p = 0; p < num; p++) {
        // get I/O offset depend by strides
        int32_t inOffset = 0;
        int32_t outOffset = 0;
        for (int32_t d = 3; d < ndims; ++d) {
            inOffset += setCoords[d] * sp->dims[d].inStrides;
            outOffset += setCoords[d] * sp->dims[d].outStrides;
        }
        half* inUp = input + inOffset;
        half* outUp = output + outOffset;

        for (int32_t z = 0; z < sp->dims[2].inDims; z++) {
            int32_t y = sp->dims[1].inDims & (~(SMAXIS0LINESCHUNK - 1));
            if (y) {
                softmaxFunc(outUp, inUp, w, tailMask8Lines, padMask8Lines, sp->dims[1].inStrides << 1,
                            sp->dims[1].outStrides << 1, y);
            }
            half* in = inUp + sp->dims[1].inStrides * y;
            half* out = outUp + sp->dims[1].outStrides * y;
            for (; y < sp->dims[1].inDims; y++) {
                smAsmLineCalcSoftmax(out, in, w, tailMask, padMask);
                in += sp->dims[1].inStrides;
                out += sp->dims[1].outStrides;
            }
            inUp += sp->dims[2].inStrides;
            outUp += sp->dims[2].outStrides;
        }

        // increment coordinates, same in-out size by operation definition
        for (int32_t d = 3; d < ndims; ++d) {
            if (setCoords[d] < sp->dims[d].inDims - 1) {
                setCoords[d]++;
                break;
            }
            setCoords[d] = 0;
        }
    }
}
